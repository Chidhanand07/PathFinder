class Pathfinder:
    def __init__(self, matrix):
        self.matrix = matrix
        self.grid = Grid(matrix=matrix)
        self.select_surface = p.image.load('selection.png').convert_alpha()

        self.path = []

    def draw_active_cell(self):
        mouse_pos = p.mouse.get_pos()
        row, col = mouse_pos[1] // 32, mouse_pos[0] // 32
        current_cell_value = self.matrix[row][col]
        if current_cell_value == 1:
            rect = pygame.Rect((col * 32, row * 32), (32, 32))
            screen.blit(self.select_surface, rect)

    def create_path(self):
        start_x, start_y = [1, 1]
        start = self.grid.node(start_x, start_y)

        mouse_pos = p.mouse.get_pos()
        end_x, end_y = mouse_pos[0] // 32, mouse_pos[1] // 32
        end = self.grid.node(end_x, end_y)

        finder = AStarFinder(diagonal_movement=DiagonalMovement.always)
        self.path, _ = finder.find_path(start, end, self.grid)
        print("Path:", [(node.x, node.y) for node in self.path])
        self.grid.cleanup()

    def draw_path(self):
        if self.path:
            points = []
            for point in self.path:
                x = (point.x * 32) + 16
                y = (point.y * 32) + 16
                points.append((x, y))

            p.draw.lines(screen, '#FF0000', False, points, 7)

    def update(self):
        self.draw_active_cell()
        self.draw_path()


